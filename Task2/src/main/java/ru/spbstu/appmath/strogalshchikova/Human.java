package ru.spbstu.appmath.strogalshchikova;

public class Human {
    private final int age;
    private final String name;

    public Human(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}

